function normalizeLang(lang: string): AppLang {
  if (lang === "en" || lang === "ja" || lang === "ko" || lang === "zh") return lang;
  return "vi";
}

function replaceParams(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return Object.entries(params).reduce(
    (value, [key, param]) => value.replace(new RegExp(`\\{${key}\\}`, "g"), String(param)),
    template
  );
}

export function translateText(key: string, lang: AppLang, params?: Record<string, string | number>) {
  const fallback = dictionaries.vi[key] || key;
  const value = dictionaries[lang][key] || fallback;
  return replaceParams(value, params);
}

export function getCurrentLang() {
  return normalizeLang(getAppLanguage());
}

export function useAppI18n() {
  const [lang, setLang] = useState<AppLang>("vi");

  useEffect(() => {
    const sync = () => setLang(getCurrentLang());
    sync();
    window.addEventListener("storage", sync);
    window.addEventListener("app-language-change", sync as EventListener);
    return () => {
      window.removeEventListener("storage", sync);
      window.removeEventListener("app-language-change", sync as EventListener);
    };
  }, []);

  return useMemo(
    () => ({
      lang,
      t: (key: string, params?: Record<string, string | number>) => translateText(key, lang, params),
    }),
    [lang]
  );
}

export function translatePoi<T extends {
  id: string;
  category?: string;
  short_description?: string;
  description?: string;
  priceText?: string;
}>(poi: T, lang: AppLang): T {
  if (lang === "vi") return poi;
  const translated = poiTranslations[poi.id]?.[lang];
  if (!translated) return poi;
  return {
    ...poi,
    category: translated.category || poi.category,
    short_description: translated.short_description || poi.short_description,
    description: translated.description || poi.description,
    priceText: translated.priceText || poi.priceText,
  };
}

export function translatePois<T extends {
  id: string;
  category?: string;
  short_description?: string;
  description?: string;
  priceText?: string;
}>(pois: T[], lang: AppLang) {
  return pois.map((poi) => translatePoi(poi, lang));
}

